               <font color="red"> <h1>1.Présentation du métier </h1></font>
<br>Le vendeur en pharmacie est un intermédiaire pharmaceutique qui seconde le pharmacien dans le service et le conseil à la clientèle. Bras droit du pharmacien, il délivre les médicaments et appareillages médicaux et autres (pommades, gélules, solutions…) ; il gère les stocks en contrôlant le réapprovisionnement, reçoit et range les commandes, met à jour les rayons de produits (produits pharmaceutiques, diététiques, cosmétiques d’hygiène, de parfumerie…) ; et en contribue à la bonne marche de l’officine en exécutant les consignes du pharmacien (accomplissement des tâches administratives, exécution des travaux à l’avant comme à l’arrière de l’officine, conseil et vente). Le vendeur en pharmacie est un technicien qui possède des connaissances générales en pharmacologie et est imprégné de la législation sur les médicaments et la pharmacologie. Il connaît les médicaments et leur usage, et conseille le client sur l’application de la prescription médicale conformément à l’ordonnance. Il assume aussi une fonction commerciale auprès de la clientèle à laquelle il propose et vend des produits de la pharmacie (produits et accessoires, d’hygiène corporelle, de diététiques…)

<br><font color="red">2.Exercice du métier </font>
<br>Le vendeur en pharmacie exerce dans toute officine ou local où les médicaments sont préparés, conservés et distribués au détail par le pharmacien, et où on procède à l’exécution des ordonnances médicales. Il exerce également dans les grandes surfaces et supermarchés dans les rayons cosmétiques vendus en pharmacie, dans l’agroalimentaire (alimentation des bébés) ; et plus globalement dans les parapharmacies en tant que commerces à part entière (moyennes surfaces implantées au centre-ville ou dans les centres commerciaux.
<br><font color="red">3.Objectifs :</font>
<br>*	Assure la prise en charge totale de la clientèle
<br>*	Maitrise les caractéristiques et les propriétés, les avantages et les inconvénients des produits de confort et libre accès de la pharmacie parapharmacie 
<br>*	Organise des événementiels, anime l’espace de vente.
<br><font color="red">4.Mission  </font>
<br>Le vendeur en pharmacie a pour rôle d’assister le pharmacien. Il participe à l’accueil des clients en officine, à l’identification des prescriptions médicales, à la délivrance des articles pharmaceutiques, au conseil et à l’orientation des malades il contribue aussi à la gestion de l’officine.

<br><font color="red">5.Compétence professionnelles acquises  </font>
<br>*	Connaissance des molécules pharmaceutiques et leurs usages 
<br>*	Application générale de la légalisation sur les médicaments et la pharmacologie 
<br>*	Maîtrise des nouveautés en matière de médicaments, d’usage et de soins
<br>*	Capacité à étiqueter et ranger les produits pharmaceutiques (recevoir, étiqueter, classer)
<br>*	Conseil clientèle sur l’usage des médicaments conformément à l’ordonnance médicale 
<br>*	Tenue des stocks et des rayons, et en informatisation de la gestion minimale des stocks 
<br>*	Opérations de service après-vente (échange, retour fournisseur, remboursement, envoie en réparation…)
<br>*	Gestion de la caisse (procédures d’encaissement, ouverture et fermeture de la caisse)
<br>*	Gestion informatisée de la caisse (Sage saisie de caisse décentralisée)
<br>*	Techniques de vente et argumentation commerciale
<br>*	Insertion professionnelle et techniques de communication (Aptitude à l’informatique et Internet ; outil et techniques de recherche d’emploi ; structure et organisation de l’officine)
<br>*	L’entrepreneuriat

<br><font color="red">6.Conditions d’accès </font>
<br>Être âgée d’au moins 17 ans au moment de l’inscription
<br>BEPC, Probatoire, BAC
<br>Justifier de l’aptitude physique et mentale 
<br>Subir avec succès le test de sélection
<br>Durée et contenu de formations 
<br>La formation dure 12 mois


<br><font color="red">7.Organisation de la formation </font>

<br>La formation se déroule selon un mode d’alternance entre une formation théorique et pratique à l’institut des stages en officine. L’affectation et le suivi des stagiaires sont assurés par la direction pédagogique de l’institut.

<br><font color="red">8.LE DIPLOME ET FINALITE DE LA FORMATION </font>
<br>La formation sera sanctionnée par un Diplôme de qualification Professionnel (DQP) et autant de certificat de compétence que d’unité d’enseignement, reconnu par le ministère de l’emploi et de la formation professionnelle. Pour obtenir ce DQP, le candidat doit suivre la formation dans sa totalité, soutenir un mémoire professionnel et satisfaire à l’examen final et ou valider tous les certificats de compétence de l’option choisie. . .




