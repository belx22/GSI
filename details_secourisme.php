               <br><font color="red"> 1. DEFINITIONS DU METIER</font>
<br>Un secouriste est une personne formée pour sauvegarder la vie d’une victime avant l’arrivée des
personnes plus spécialisées.
Il mène de missions de prévention auprès du public et offre un appui aux victimes en cas de sinistre.
Derrière ce terme générique se cache des multiples réalités qui, toutes se regroupent sous l’idée
d’assistance à personne en danger.
Un métier facilement accessible (même aux bénévoles sous conditions d’un grand sang-froid et d’un
solide sens de responsabilités.
<br><font color="red">2. FORMATION</font>
<br>Niveau requis : BEPC, CAP GCE OL
DUREE : 12 Mois (7 Mois de cours théorique et 5 Mois de cours pratique en milieu hospitalier)
ADMISSION : sur étude de dossier<br>
<br><font color="red">3. LES MISSIONS DU SECOURISTE :</font>
- Participer à des dispositifs préventifs de secours lors d’évènements de grande envergure, de
manifestations sportives ou culturelles ;
- Assurer le respect des règles de sécurité sur des lieux publics ;
- Intervenir en cas de sinistre, d’accident, pour porter secours aux victimes et préparer l’arrivée du
médecin ;
- Poser du matériel de délimitation de la zone et de protection
<br><font color="red">4. COMPETENCES ATTENDUES</font><br>
Le secouriste est un professionnel endurant, résistant sur le plan physique et émotionnel. Son sens de
l’observation et de l’analyse rend possible une compréhension rapide et précise de la situation à
laquelle il est confronté.
Il est réactif, adaptable et dynamique. Il sait faire preuve de sang-froid ainsi que d’initiative.
Le secouriste exerce au contact de personnes blessées, stressées, fragilisées.
Il doit développer certaines qualités humaines, telles que l’empathie et le sens de l’écoute. Il sait se
montrer rassurant et clair, faire preuve de pédagogie et de diplomatie
Enfin il possède un grand sens de l’équipe
<br><font color="red">5. DEBOUCHES :</font><br>
Un secouriste peut travailler
- Milieu hospitalier et extrahospitalier
- Dans les ONG,
- Maison d’accueil spécialisée, établissement et service d’aide par le travail (ESAT)
- Ambulanciers ;
- Tout secteur de sécurité
- Etc…
<br><font color="red">6. DIPLOME ET FINALITE</font><br>
La formation sera sanctionnée par un Certificat de Qualification Professionnelle (CQP) décerné par le
Ministère de l’Emploi et de la Formation Professionnelle (MINEFOP)


