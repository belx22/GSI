               <font color="red"> 1.Description de la formation</font><br>
L’auxiliaire Généraliste de Santé est un professionnel de santé qui travaille sous la supervision d’un infirmier, d’un cadre supérieur ou d’un médecin, dans le cadre du rôle propre dévolu à celui-ci, conformément au Code de la Santé Publique. Dans ce cadre, l’Auxiliaire Généraliste de santé réalise des soins liés aux fonctions d’entretien et de continuité de vie visant à compenser partiellement ou totalement un manque ou une diminution de l’autonomie de la personne ou d’un groupe de personnes. Son rôle s’inscrit dans une approche globale de la personne soignée et prend en compte la dimension relationnelle des soins. L’Auxiliaire Généraliste de santé accompagne cette personne dans les activités de la vie quotidienne, il contribue à son bien-être et à lui recouvrer, dans la mesure du possible, son autonomie. Travaillant le plus souvent dans une équipe pluri professionnelle, en milieu hospitalier ou extrahospitalier, l’Auxiliaire généraliste de santé participe, dans la mesure de ses compétences et dans le cadre de sa formation aux soins infirmiers préventifs, curatifs ou palliatifs. Ces soins ont pour objet de promouvoir, protéger, maintenir et restaurer la santé de la personne, dans le respect de ses droits et de sa dignité.
<br><font color="red">2.Activités principales</font><br>
Les activités principales de l’auxiliaire généraliste de santé peuvent se regrouper en 8 pôles :<br>
1.	Dispenser des soins de santé et de confort à la personne <br>
2.	Observer la personne et mesurer les principaux paramètres liés à son état de santé <br>
3.	Aider l’infirmier à la réalisation de soins <br>
4.	Assurer l’entretien de l’environnement immédiat de la personne et la réfection des lits <br>
5.	Entretenir des matériels de soins <br>
6.	Transmettre ses observations par oral et par écrit pour maintenir la continuité des soins <br>
7.	Accueillir, informer et accompagner les personnes et leur entourage <br>
8.	Accueillir et accompagne des stagiaires en formation.<br>
De manière plus spécifique, il s’agit de :<br>
•	Accueillir le malade <br>
•	Installer le malade <br>
•	Rassurer le patient <br>
•	Orienter le patient si possible <br>
•	Ranger et si possible stériliser le matériel <br>
•	Aider à préparer le matériel <br>
•	Aider à faire les soins infirmiers<br>
•	Préparer et surveiller les soins du malade ou des traitements spécifiques sous le contrôle d’un cadre Infirmier<br>
•	Déterminer et expliquer la contre-indication aux différents traitements et examens <br>
•	Déterminer et expliquer la conduite à tenir sur les effets secondaires probables du traitement ou des soins.<br>
<br>•	Assurer l’entretien du matériel, la propreté de la salle (nettoyage stérilisation les procédures d’élimination des déchets)
•	Enregistrer les données liées à l’activité <br>
•	Aider dans la mise en œuvre des protocoles de différents soins <br>
<br>•	Appliquer les mesures de prévention dans tous les soins et au sein de la structure hospitalière (infections nosocomiales)
•	Accueillir et encadrer les autres personnels de santé les différents stagiaires dans les services.<br>
•	Encadrer les autres personnels de santé <br>
•	Faire toujours l’IEC aux malades ou à sa famille pendant ou après le séjour.<br>

<font color="red">3.Relations professionnelles les plus fréquentes</font><br>
Travailler en étroite collaboration avec l’équipe soignante et dirigeante <br>

<font color="red">4.Savoir-faire requis</font><br>
