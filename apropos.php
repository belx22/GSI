                    En fait, ce qui est au centre de l’approche au plan économique et qui est aussi sa plus grande réussite, c’est « la prise en charge paramédicale », soit l’appropriation des moyens par les populations et le fait qu’elle conserve la pleine maîtrise sur leurs actifs. Ainsi le développement des ressources financières autonomes contrôlées par la collectivité constitue un élément essentiel du développement paramédical.
