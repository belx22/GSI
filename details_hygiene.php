<p>
               <h1><font color="red">1.Présentation de la formation </font></h1>
	<br>La présente formation vise à développer chez le futur technicien d’hygiène et de salubrité publique, des compétences requises pour sauvegarder, améliorer et restaurer les conditions favorables à l’environnement et à la qualité de vie, contribuant ainsi à la bonne santé de l’individu, de la famille et de la communauté.
	<br>Il s’agit d’une formation, à vocation professionnelle, qui a pour objectif de fournir les connaissances techniques scientifiques, juridiques et humaines permettant une approche pluridisciplinaire des différents aspects de l’Hygiène et de la salubrité publique et de l’impact de l’entreprise sur l’environnement.
	<br>En matière 	d’hygiène publique l’Etat a imposé par un texte l’obligation pour la commune de disposer d’un tel service. Les compétences du service sont également définies par l’Etat. Dans la plupart des cas l’Etat met d’ailleurs aussi un agent du Ministère de la Santé à la disposition de la commune.

   <br><h1><font color="red">2.Objectifs généraux </font></h1>
   <br>La formation va rendre les apprenants capables, dans le respect des sensibilités culturelles, de :
      <br>1.	Participer à la conception des infrastructures d’eau et d’assainissement ;
      <br>2.	Assurer l’inspection des unités industrielles, les établissements hôteliers, établissements publics …
      <br>3.	Assurer la lutte antivectorielle 
      <br>4.	Contrôler et superviser la désinfection des lieux publics et agro-alimentaires ;
      <br>5.	Effectuer les activités de prévention et d’éducation dans les communautés
      <br>6.	Gérer les ressources humaines, matérielles et financières mises à sa disposition ;
      <br>7.	Participer à la formation, à l’encadrement et la recherche opérationnelle.


