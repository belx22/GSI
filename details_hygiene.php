<br><h1><font color="red">8.Méthodologie </font></h1>
<br>La formation se donnera de manière participative et interactive. Des exercices pratiques seront au cœur de l’approche.
<br>Basés sur des cas réels apportés par les participants ou, en cas de besoin, sur des cas standards, ces exercices permettront d’assimiler les différents modèles théoriques et les instruments exposés par les formateurs et de faire le lien entre ces sessions théoriques et le travail quotidien.
<br>Les discussions entre les participants et les échanges d’expérience et de savoir-faire seront stimulées par les formateurs pendant ce processus. Les méthodes participatives et interactives permettront de :
   <br>•	Prendre en compte les attentes des apprenants ;
<br>•	Alterner des moments théoriques avec des moments pratiques, en y intégrant les discussions entre les apprenants ;
<br>•	Alterner les ateliers en sous-groupes avec du brainstorming individuel 
<br>•	Diriger les interventions des apprenants : pouvoir intervenir au bon moment.
<br>D’une manière générale les méthodes suivantes seront employées.
<br>•	Apports théoriques 
<br>•	Analyse d’expériences 
<br>•	Travaux dirigés en petits groupes et construction d’outils communs.
<br>•	Analyse critique de documents institutionnels
<br>•	Rencontres avec des acteurs de l’intelligence économique.
<br>•	Alternance de travaux de groupes et d’apports méthodologiques et théoriques à travers des études de cas et l’élaboration d’outils ;
<br>•	Interventions externes diversifiées sous forme de textes, vidéo et débats.

<br><h1><font color="red">9.LA PEDAGOGIE</font> </h1>
<br>Il s’agit de formation action caractérisée par :
   <br>•	Un apprentissage basé sur l’action (études de cas, mises en situation, exercices de prises de décisions)
<br>•	Les récits d’expérience apportés par des spécialistes ‘terrain’ confrontés à la réalité quotidienne des entreprises 
<br>•	Les inputs des chercheurs scientifiques apportant des visions novatrices 
<br>•	Le coaching individuel visant à garantir la qualité de la mise en œuvre 
<br>•	Une pédagogie efficace, tournée vers l’action et très interactive qui offre l’opportunité de découvrir en un temps compact les meilleures pratiques du moment, rencontrer et dialoguer directement avec des chercheurs et experts de premier plan et rencontrer des entreprises qui mettent en œuvre réellement.
<br>C’est aussi une « formation-action » avec un transfert immédiat des acquis vers son propre projet. Des études de cas, des mises en situation et des exercices de prises de décision mettent en évidence le potentiel des pratiques et des outils.
<br>Il s’agit donc d’une pédagogie active qui mobilise des capacités d’initiative, d’autonomie et de responsabilité des apprenants : cours, séminaires, études de cas, travaux individuels ou collectifs, etc.
<br>La pédagogie est aussi basée sur un dispositif d’accompagnement personnalisé. Le mise en cohérence des savoirs et les réalités du terrain sera possible et d’autant plus efficace que l’apprenant bénéficiera d’un dispositif d’accompagnement. Pour répondre aux spécificités des porteurs de projet (origine géographique, nature du projet …), l’accompagnement dans le cadre de la présente formation mobilise :
   <br>•	Un « Tuteur expert dans un domaine de spécialité » qui apportera son expertise à l’apprenant et l’accompagnera dans les différentes phases du processus de formulation de son projet professionnel. Il s’agit d’un praticien en activité, qui va guider le stagiaire sur le terrain, lui ouvrir ses réseaux, lui faciliter l’accès aux administrations, aux financeurs potentiels, aux partenaires possibles ;
<br>•	Un « Tuteur académique » Sa fonction est de favoriser la synthèse entre les enseignements et la pratique du terrain, d’aider l’apprenant porteur d’un projet professionnel, à mobiliser les concepts et outils dans sa démarche de formulation et à élaborer un mémoire de projet qui réponde aux exigences académiques (maîtrise des outils théoriques, normes de présentation) et professionnelles.
<br>•	Des « experts » techniques, juridiques … sollicités en fonction des besoins, au moment opportun pour apporter une expertise technique en relation avec l’activité support de professionnalisation de l’apprenant.

<br><h1><font color="red">10.LE DIPLOME ET FINALITE DE LA FORMATION   </font></h1>
<br>La formation sera sanctionnée par un certificat de qualification Professionnel et autant de certificat de compétence que d’unité d’enseignement, reconnu par le ministère de l’emploi et de la formation professionnelle. Pour obtenir ce certificat de qualification professionnel, le candidat doit suivre la formation dans sa totalité, soutenir un mémoire professionnel et satisfaire à l’examen final et ou valider tous les certificats de compétence de l’option choisie. . .


