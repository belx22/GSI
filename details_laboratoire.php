               <font color="red">1.	Objectif de la formation</font> <br>
Cette formation vise à former des auxiliaires de laboratoire, aide chimiste ou aide laborantin à même assister le technicien de laboratoire à détecter par des examens en laboratoire, les différentes pathologies pouvant affecter le corps humain.
<br><font color="red">2.	Compétences recherchées </font><p>
Compétences génériques <br>
•	Aider à réceptionner et contrôler le prélèvement biologique et consigner la date, l’heure de prélèvement, les coordonnées du patient, etc.
<br>•	Aider à préparer les analyseurs, les réactifs et échantillon biologiques (fractionnement, répartition selon les types d’analyse)
<br>•	Aider à procéder au traitement de l’échantillon par centrifugation, dilution, chauffage, et surveiller le déroulement de l’analyse et consigner les données ;
<br>•	Aider à suivre l’état des stocks des consommables, établir les commandes de réapprovisionnement et effectuer le rangement du matériel selon leur date de validité et leurs conditions de conservation ;
<br>•	Aider et vérifier le fonctionnement des analyseurs, actualiser le registre de maintenance et informer en cas de dysfonctionnement ;
<br>•	Aider à désinfecter, décontaminer, ranger le poste de travail et le matériel ;
<br>•	Aider à procéder à l’évacuation et à la destruction des déchets.
   Compétences spécifiques<br>
